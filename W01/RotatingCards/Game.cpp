void Game::ClearBackground( ) const
{
	glClearColor( 0.0f, 0.0f, 0.3f, 1.0f );
	glClear( GL_COLOR_BUFFER_BIT );
}

void Game::InitialiseCards()
{
	const float radius{ 200.0f };
	const float angleSteps{ 360.0f / Card::m_MaxRank };
	const Circlef circle{ m_Window.width / 2, m_Window.height / 2, radius };
	for (size_t card{ 0 }; card < Card::m_MaxRank; ++card)
	{
		m_Cards.push_back(new Card(Card::Suit::hearts, (int)card + 1, angleSteps * (int)card, circle));
	}
}

void Game::DrawCards() const
{
	for (Card* card : m_Cards)
		card->Draw();
}